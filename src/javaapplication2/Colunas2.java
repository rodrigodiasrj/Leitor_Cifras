    public void Ler(File lista) throws IOException {

        label2.setText(index + 1 + "/" + indexM);
        File arquivoLeitura = new File(lista.getAbsolutePath());

        // pega o tamanho
        long tamanhoArquivo = arquivoLeitura.length();
        FileInputStream fs = new FileInputStream(arquivoLeitura);
        DataInputStream in = new DataInputStream(fs);

        int numLinhas;
        LineNumberReader lineRead = new LineNumberReader(new InputStreamReader(in));
        lineRead.skip(tamanhoArquivo);
        // conta o numero de linhas do arquivo, comeÃ§a com zero, por isso adiciona 1
        numLinhas = lineRead.getLineNumber();

        try {
            BufferedReader leitor = new BufferedReader(new InputStreamReader(new FileInputStream(lista), "UTF-8"));

            String linha = "";
            String aux = "";
            String aux2 = "";
            int i = 0, j = numLinhas;

            while ((linha = leitor.readLine()) != null) {
                if (i < (j / 2)) {
                    aux += linha;
                    aux += "\n";
                    ++i;
                } else {
                    aux2 += linha;
                    aux2 += "\n";
                }
            }
            jTextPane1.setText(aux);
            jTextPane2.setText(aux2);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Colunas2.class.getName()).log(Level.SEVERE, null, ex);
        }

        setExtendedState(MAXIMIZED_BOTH);
        StyledDocument doc = jTextPane1.getStyledDocument();
        SimpleAttributeSet center = new SimpleAttributeSet();
        StyleConstants.setAlignment(center, StyleConstants.ALIGN_CENTER);
        doc.setParagraphAttributes(0, doc.getLength(), center, false);

        jTextPane1.setFont(new Font("", Font.PLAIN, fontTam));
        jTextPane1.setEditable(false);

        StyledDocument doc1 = jTextPane2.getStyledDocument();
        SimpleAttributeSet center1 = new SimpleAttributeSet();
        StyleConstants.setAlignment(center1, StyleConstants.ALIGN_CENTER);
        doc.setParagraphAttributes(0, doc.getLength(), center, false);
        jTextPane2.setFont(new Font("", Font.PLAIN, fontTam));
        jTextPane2.setEditable(false);

    }

